import java.util.*;
public class Mul {
    public static void printMul(int a,int b){
        int mul=a*b;
        System.out.println(mul);
    }
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        int a=sc.nextInt();
        int b=sc.nextInt();
        printMul(a,b);
    }

    
}
